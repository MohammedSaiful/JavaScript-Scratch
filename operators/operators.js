
// Arithmetic Operators
console.log(10 + 5);
console.log(10 - 5);
console.log(10 * 5);
console.log(2 ** 3);
console.log(10 / 2);
console.log(10 % 3);

let x = 5;
console.log(--x);
console.log(x);

console.log(x++);
console.log(x);


let y = 5;
console.log(--y);
console.log(y);

console.log(y--);
console.log(y);


// Operator  Description                    Example                         Output
//    +        Addition               console.log(10 + 5)                     15
//    -        Subtraction            console.log(10 - 5)                     5
//    *        Multiplication         console.log(10 * 5)                     50
//    **        Exponentiation        console.log(2 ** 3)                     8
//    /        Division               console.log(10 / 2)                     5
//    %        Modulus (Remainder)    console.log(10 % 3)                     1
//    ++        Increment               let x = 5; x++; console.log(x)        6
//    --        Decrement               let x = 5; x--; console.log(x)         4



// Assignment Operators

//       Operator,       Example,       Equivalent To,         Output
//       =,              x = 10,             x = 10,                 10
//       +=,              x += 5,             x = x + 5,           15
//       -=,              x -= 5,             x = x - 5,           5
//       *=,              x *= 2,             x = x * 2,            20
//       /=,              x /= 2,             x = x / 2,           5





//        Operator,          Description,            Example,                            Output
//        ==,         Equal to (Value only),"           console.log(5 == ""5"")              true
//        ===,        Strict equal (Value & Type),"     console.log(5 === ""5"")           false
//        !=,         Not equal,                        console.log(5 != 8)                 true
//        !==,        Strict not equal,"                console.log(5 !== ""5"")              true
//        >,          Greater than,                     console.log(10 > 5)                  true
//        <,          Less than,                        console.log(5 < 10)                 true
//        >=,         Greater than or equal,            console.log(10 >= 10)               true
//        <=,         Less than or equal,                console.log(5 <= 10)              true




// Bitwise Operators
console.log(5 & 1);
console.log(5 | 1);
console.log(~5);
console.log(5 ^ 1);
console.log(5 << 1);
console.log(5 >> 1);

//   Operator	Description	   Example	            Output
//     &	      AND	        console.log(5 & 1)  	1
//     |	      OR	        console.log(5 | 1)  	5
//     ~	      NOT	        console.log(~5)	        -6
//     ^	      XOR	        console.log(5 ^ 1)  	4
//     <<	      Left Shift	console.log(5 << 1) 	10
//     >>	      Right Shift	console.log(5 >> 1) 	2