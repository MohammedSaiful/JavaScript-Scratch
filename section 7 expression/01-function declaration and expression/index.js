// Function Declaration
function myFunction(x, y) {
  return x * y;
}

// Function Expression (Named)
const myFunction = function name(x, y) {
  return x * y;
};

// Function Expression (Anonymous)
const myFunction = function (x, y) {
  return x * y;
};

// Arrow Function
const myFunction = (x, y) => x * y;
